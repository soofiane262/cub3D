#include "libft.h"

void	ft_bzero(void *x, size_t len)
{
	size_t			j;
	unsigned char	*dst;

	dst = (unsigned char *)x;
	j = 0;
	while (j < len)
	{
		*dst = '\0';
		dst++;
		j++;
	}
}
